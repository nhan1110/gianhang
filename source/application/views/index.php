<script type="text/javascript">
    $(document).ready(function () {
        $(document).on('click', '.btn-show-filter', function () {
            if ($(this).hasClass('opens')) {
                $('#page-search .filter-left').stop().slideUp('slow');
                $(this).removeClass('opens');
            } else {
                $('#page-search .filter-left').stop().slideDown('slow');
                $(this).addClass('opens');
            }
            return false;
        });

        $(document).on('click', '.close-filter', function () {
            $('#page-search .filter-left').stop().slideUp(100);
            $('.btn-show-filter').removeClass('opens');
            return false;
        });
    });
</script>